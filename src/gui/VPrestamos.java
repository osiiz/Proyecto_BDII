    private void btnPrestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrestarActionPerformed
        ModeloTablaPrestamos mp;
        mp=(ModeloTablaPrestamos) tablaPrestamos.getModel();
        fa.prestarEjemplar(ejemplar.getNumEjemplar(), ejemplar.getIdLibro(), (String) mp.getValueAt(tablaPrestamos.getSelectedRow(), 0));
        vlibro.actualizarEjemplares();
        this.dispose();
    }//GEN-LAST:event_btnPrestarActionPerformed

