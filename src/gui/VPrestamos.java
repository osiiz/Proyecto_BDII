    private void tablaPrestamosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPrestamosMouseClicked
        ModeloTablaPrestamos mp;
        mp=(ModeloTablaPrestamos) tablaPrestamos.getModel();
        if ((Integer) mp.getValueAt(tablaPrestamos.getSelectedRow(), 3) == 0){
            btnPrestar.setEnabled(true);
        }else{
            btnPrestar.setEnabled(false);
        }
    }//GEN-LAST:event_tablaPrestamosMouseClicked

    public void buscaUsuarios(){
        ModeloTablaPrestamos mp;
        mp=(ModeloTablaPrestamos) tablaPrestamos.getModel();
        mp.setFilas(fa.obtenerPrestamos(textoId.getText(), textoNombre.getText(), ejemplar.getNumEjemplar()));
    }
    

